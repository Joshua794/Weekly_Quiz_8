import math

#Basic Operations
def add (a, b):
    return a+b

def sub (a, b):
    return a-b

def mult (a, b):
    return a*b

def div (a, b):
    return a/b

#Advanced Operations
def log(a, b):
    return math.log(a, b)

def square(a):
    return a**2

def sin(a):
    return math.sin(a)

def cos(a):
    return math.cos(a)

def sqrt(a):
    return math.sqrt(a)

def percentage(a, b):
    return (a / b) * 100
